	<script>
		function toggleNav() {
			var el = document.getElementById('menu');

			if ( el.style.display != 'block' ) {
				el.style.display = 'block';
			} else if (el.style.display != 'none') {
				el.style.display = 'none';
			}
		}
	</script>
